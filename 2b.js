const rules = {
	rock: 'scissors',
	scissors: 'paper',
	paper: 'rock'
};

const loseRules = {
	rock: 'paper',
	scissors: 'rock',
	paper: 'scissors'
}

const playerMap = {
	A: 'rock',
	B: 'paper',
	C: 'scissors',
	X: 'lost',
	Y: 'draw',
	Z: 'won'
};

const scores = {
	rock: 1, paper: 2, scissors: 3,
	lost: 0, draw: 3, won: 6
};

function result(a, b) {
	if (b == 'draw')
		return a;
	if (b == 'lost')
		return rules[a];
	return loseRules[a];
}

function play(input) {
	const score = input.split('\n').filter(n => isNaN(n))
		.map(game => {
			const [a, b] = game.split(/\s+/).map(p => playerMap[p]);
			const res = result(a, b);
			const points = scores[b] + scores[res];
			return points;
		})
		.reduce((a, b) => a + b, 0)
	console.log(score)
}

