const diffLowercase = 96;
const diffUppercase = 64 - 26;

function split(s) {
	const first = s.substr(0, s.length / 2)
	const second = s.substr(s.length / 2);
	return [first, second];
}

function findCommon(first, second) {
	for (const s of first) {
		if (second.includes(s)) {
			return s;
		}
	}
}

function points(common) {
	const diff = common.toUpperCase() === common ? diffUppercase : diffLowercase;
	return common.charCodeAt(0) - diff;
}

function count(input) {
	const result = input
		.split('\n').filter(a => a.length > 0)
		.map(s => split(s))
		.map(pair => findCommon(pair[0], pair[1]))
		.map(common => points(common))
		.reduce((a, b) => a + b, 0)

	console.log(result);
}

